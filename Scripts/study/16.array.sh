#!/bin/sh
#用于对数组使用的说明
#1.创建数组
echo "创建数组"
A[0]=5
A[1]=1
A[2]=28
B=(12 33 223 "HELLO")

#2.显示数组
echo "显示数组"
echo "${A[0]}"
echo "${A[1+1]}"
echo "${B[3]}"

#3.取出数组所有的元素
echo "取出数组的所有元素"
echo "${A[@]}"
echo "${B[*]}"

#4.取出数组中元素的个数/单个元素的长度
echo "取出数组中元素的个数/单个元素的长度"
echo "${#A[@]}"
echo "${#A[*]}"
echo "${#B[3]}"

#取消数组/数组元素
echo "取消数组/数组元素"
unset A
unset B[3]

